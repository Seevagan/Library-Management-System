        try {
            while(rs.next())
            {
              value = rs.getString("tot");
            } 
            
        } catch (SQLException ex) {
            Logger.getLogger(CheckOut.class.getName()).log(Level.SEVERE, null, ex);
        }
       // System.out.println("after ex");
        int count = Integer.parseInt(value);
        System.out.println(count);
